                    <p> <strong>Kiállítás a felújításról, a Füvészkert épületeinek</strong>, köztük a több mint 150 éves
                        Pálmaháznak a <strong>történetéről</strong>.</p>
                    <p> A <strong>Biológiai Sokféleség Nemzetközi Napja</strong> alkalmából kiállítást rendeztünk: ennek
                        egyik része posztereken, tablókon mutatta be a Föld növényvilágának, valamint a
                        <strong>Füvészkert élőnövény-gyűjteményének sokféleségét</strong>, másik részét a Füvészkert
                        <strong>18-19. századból való botanikai könyveiből</strong> válogattuk, szemléltetve, hogy az
                        elmúlt századok idején hogyan örökítették meg a növényvilág változatosságát. </p>
                    <p> <strong>Kulturális Örökség Napjain</strong> kiállítás „200 millió éves séta a növényvilág

            <h3>1. Hatékony és széleskörű ismeretterjesztő, természetvédelemre nevelő munka szervezése, irányítása, az
                információs rendszerek fejlesztése</h3>
            <ul>
                <li>
                    <h4>Kerti séták: ismeretterjesztő szakvezetések</h4>
                    <p> Az Eötvös Loránd Tudományegyetem biológus, ill. biológia tanár szakos hallgatói előzetes
                        bejelentkezés alapján ismeretterjesztő szakvezetéseket tartanak általános- és középiskolás
                        osztályok, szakkörök részére. Az általános kertbemutatók mellett lehetőség van speciális
                        vezetések kérésére is. Például:</p>
                    <ul style="margin-top: 0px; padding-top: 0px;">
                        <li>Magyarország növényvilága</li>
                        <li>A trópusok gyümölcsei</li>
                        <li>Séta a trópusokon az esőerdoktol a sivatagokig</li>
                        <li>Élő kövületek nyomában</li>
                        <li>A fenyvesek birodalmában.</li>
                    </ul>
                    <p> Természetesen – előzetes bejelentkezés alapján – felnőtt csoportok részére is biztosítunk
                        vezetést.</p>
                    <p> <strong>Vezetés igénylése</strong>:</p>
                    <p> telefon: 210-10-74, 314-05-35</p>
                    <p> e-mail: <a href="mailto:botanikuskert@yahoo.com">botanikuskert[kukac]yahoo.com</a>, <a

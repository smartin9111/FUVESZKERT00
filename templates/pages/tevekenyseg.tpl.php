                        <li>Elkészült egy kiadvány – „A Pál utcai fiúk kalandozásai a Füvészkertben „ című térkép, –
                            mely pontos tájékoztatást ad a Füvészkert mai állapotáról, de jelzi a még meglévő –
                            regénybeli nevezetes események helyszíneit is.</li>
                        <li>2011 őszén jelent meg a Nemzeti Civil Alapprogram támogatásával a Füvészkert térképe, mely a
                            látogatók hasznos kísérője; alapvető ismereteket nyújt a bemutatott fontosabb
                            növénygyűjteményekről, nevezetes látnivalókról.</li>
                        <li>A Füvészkertért Alapítvány biztosította 2011-ben a Magyar Biológiai Társaság tudományos
                            lapjának, a Botanikai Közlemények 98. kötetének nyomdai előkészítését.</li>
                        <li>Kiadvány a „Füvészkert, a növényi sokféleség szigete a főváros háztengerében” címen, melynek
                            célja, hogy a Füvészkert látogatóinak felhívjuk a figyelmét a növényvilág „különbözőféle
                            sokféleségére”, rokonság, életformák, földrajzi elterjedés, levélszín szerinti
                            változatosságára, valamint a fajtagyűjtemények gazdagságára. (A Zöld Forrás támogatásával.)

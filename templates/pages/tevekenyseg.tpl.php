                            href="mailto:isepy@yahoo.com">yahoo.com</a></p>
                    <p> Az Alapítvány kezdettől fogva szervezi az általános- és középiskolások, valamint az érdeklődő
                        felnőtt csoportok <strong>ismeretterjesztő szakvezetését</strong> (iskolásoknak biológia óra
                        keretében) esetenként angol, ill. német nyelven, az Egyetem biológus hallgatóinak a
                        közreműködésével.</p>
                    <p> Az élő természet megőrzésének fontosságára elengedhetetlenül fontos felhívni minden korosztály
                        figyelmét. Ennek szellemében indult új program szakképzett vezetőkkel a Füvészkertért Alapítvány
                        támogatásával, a legfiatalabb korosztály számára „<strong>Szamóca Tanoda</strong>” néven.</p>
                    <p> Kincseket rejtő vadon élő növényeink gyakorlati hasznának megismertetésére támogattuk
                        „<strong>gyógynövény ismereti tanfolyam</strong>” lebonyolítását a Budakalászi Gyógynövény
                        Kutató Intézet szakmai vezetésével.
                    </p>

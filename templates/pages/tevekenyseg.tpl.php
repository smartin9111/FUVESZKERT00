                XVIII-XIX. századból. Igyekszik beszerezni a botanikus kertek számára nélkülözhetetlen legújabb
                irodalmat is. A Füvészkertért Alapítvány az utóbbi években 250.000.- Ft értékben vásárolt botanikai,
                kertészeti szakkönyveket, többek közt a híres Londoni Kew Garden több mint 200 éve alapított
                kiadványának, a Curtis’s Magazin – nálunk teljes sorozatának a legújabb köteteit.</p>

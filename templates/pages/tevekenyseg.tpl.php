            <h3>2. Forrásteremtés, korszerűsítés</h3>
            <ul>
                <li><strong>A Füvészkertnek, mint kulturális örökségnek a védelme érdekében az építészeti értékek
                        fenntartásához, működtetéséhez, valamint a gazdaságosabb üzemeltetés célját szolgáló
                        beruházásokhoz források teremtése</strong>.</li>
                <li>Régi hiány pótlásaként látogatói korszerű illemhely tervezése.</li>
                <li>Alapítvány támogatói lehetővé tették, hogy adományaikból a Pálmaház korszerű felújítását szolgáló
                    kiviteli tervek elkészülhessenek (2011).</li>
            </ul>

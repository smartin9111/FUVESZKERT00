    <h1>Céljaink</h1>

    <div class="entry">
        <p>Az európai egyetemek több száz éves múlttal rendelkező különleges történeti értékű, kezdetben kizárólag az
            oktatást szolgáló egységei a botanikus kertek.</p>
        <p>Napjainkban azonban a nemzetközi elvárásoknak megfelelően, a botanikus kertek feladatai szerteágazóak.</p>
        <div
            style="margin-top: 20px; margin-bottom: 20px; margin-left: auto; margin-right: auto; background-color: #e7e7e7; width: 500px; border: 1px solid #d7d7d7; padding: 5px;">
            <p><strong>A Botanikus Kertek legfőbb funkciói</strong></p>
            <p><strong>(BGCI 1998)</strong></p>
            <ul>
                <li>Az élő gyűjtemények fenntartása</li>
                <li>A kihalástól veszélyeztetett fajok megőrzése a kertben (“ex situ”) és az eredeti élőhelyen (“in
                    situ”)</li>
                <li>Kutatás</li>
                <li>Oktatás, ismeretterjesztés</li>
                <li>Magbank</li>
                <li>Herbárium</li>
                <li>Kert-, tudomány- és kultúrtörténeti értékek megőrzése</li>
                <li>Keresedelmi tevékenység</li>
                <li>Botanikus kertek reklámozása</li>
                <li>Fenntartáshoz szükséges források felkutatása</li>
            </ul>
        </div>
        <p>Botanikus Kertünk (Eötvös Loránd Tudományegyetem Füvészkertje) sokoldalú működésének sikeres ellátása
            érdekében jött létre 2005 nyarán <strong>“A Füvészkertért Alapítvány”.</strong></p>
        <p>Célja a Füvészkert széleskörű, minden szintre kiterjedő oktató és ismeretterjesztő tevékenységének
            fejlesztése, támogatása, valamint a Kertnek, mint kulturális és természeti örökségnek egységes egészként
            történő megőrzése.</p>
        <p><strong>Az Alapítvány közhasznú tevékenységének keretében az alábbi feladatokat támogatja: </strong></p>
        <ul>
            <li>hatékony és széleskörű ismeretterjesztő, természetvédelemre nevelő munka szervezése, irányítása, az
                információs rendszerek fejlesztése,</li>
            <li>a Füvészkertnek, mint “élő múzeumnak” és az azt kiegészítő értékes gyűjteményeknek (könyvtár, herbárium,
                maggyűjtemény) a bővítése, fejlesztése,</li>
            <li>a Füvészkertnek, mint kulturális örökségnek a védelme érdekében az építészeti értékek fenntartásához,
                működtetéséhez, valamint a gazdaságosabb üzemeltetés célját szolgáló beruházásokhoz források teremtése,
            </li>
            <li>a Füvészkertet népszerűsítő rendezvények szervezése,</li>
            <li>a Füvészkert “pihenőpark” jellegű szerepének sikeresebbé tétele</li>
        </ul>

